    public empinfo() {
        initComponents();
        String username = Login.globalClass.empusername;
        String fname, lname, email, phone;
        try{
        Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
        con = DriverManager.getConnection("jdbc:mysql://localhost:3300/DBMS", "root", "");
        stmt = con.createStatement();
        query = "select * from employees where emp_id='" + username + "';";
        rs = stmt.executeQuery(query);
        while (rs.next()) {
            username = rs.getString("emp_id");
            fname = rs.getString("Fname");
            lname = rs.getString("Lname");
            email = rs.getString("email");
            phone = rs.getString("mobile");
            jTextField1.setText(username);
            jTextField2.setText(fname);
            jTextField3.setText(lname);
            jTextField4.setText(phone);
            jTextField5.setText(email);
            empinfo.globalClass.fname=fname;
            empinfo.globalClass.lname=lname;
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

